//Name..........: SD_ArrayPlus
//Description...: Function search number of advantages.
//Syntax........: SD_ArrayPlus($ArrayData,$Number)
//Parameters....: $ArrayData - data
//                $Number - number of image
//Return values.: number of advantages
//Author .......: ukasz Kusy
//Modified......: 13.03.2015
////////////////////////////////////////////////////////////////////////////
function SD_ArrayPlus($ArrayData,$Number)
{
   for ($i= 1;$i< $ArrayData[0][0]; $i++ )
   {

            if($Number == $ArrayData[$i][1]) return $ArrayData[$i][0] ;
   }

}
