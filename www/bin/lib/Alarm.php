//Function /////////////////////////////////////////////////////////////////
//Name..........: Alarm_Run
//Description...: Function set alarm for system.
//Syntax........: Alarm_Run($Path,$Alarm)
//Parameters....: $Path - path to file
//                $Alarm - flag of alarm
//Return values.: None
//Author .......: ukasz Kusy
//Modified......: 19.05.2015
////////////////////////////////////////////////////////////////////////////
function Alarm_Run($Path,$Alarm)
{
    $Handle = fopen($Path.$Alarm.".alarm", "w");
    fclose($Handle);
}
////////////////////////////////////////////////////////////////////////////



//Function /////////////////////////////////////////////////////////////////
//Name..........: Alarm_End
//Description...: Function end alarm for system.
//Syntax........: Alarm_End($Path,$Alarm)
//Parameters....: $Path - path to file
//                $Alarm - flag of alarm
//Return values.: None
//Author .......: ukasz Kusy
//Modified......: 19.05.2015
////////////////////////////////////////////////////////////////////////////
function Alarm_End($Path,$Alarm)
{
    unlink($Path.$Alarm.".alarm");
}
////////////////////////////////////////////////////////////////////////////



//Function /////////////////////////////////////////////////////////////////
//Name..........: Alarm_Get
//Description...: Function end alarm for system.
//Syntax........: Alarm_End($Path,$Alarm)
//Parameters....: $Path - path to file
//Return values.: return true - alarm is set
//                       false - alarm in'nt set
//Author .......: ukasz Kusy
//Modified......: 19.05.2015
////////////////////////////////////////////////////////////////////////////
function Alarm_Get($Path)
{
    return file_exists($Path.'ALARM_MAIN.alarm');
}
