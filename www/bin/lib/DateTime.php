//Function /////////////////////////////////////////////////////////////////
//Name..........: DT_Convert
//Description...: Function convert y-m-d to d-m-y.
//Syntax........: DT_Convert($Date)
//Parameters....: $Date - date
//Return values.: Convert date
//Author .......: £ukasz Kusy
//Modified......: 14.03.2015
////////////////////////////////////////////////////////////////////////////
function DT_Convert($Date)
{
    $Result = date("d-m-Y", strtotime($Date));

    return $Result ;
}
////////////////////////////////////////////////////////////////////////////



//Function /////////////////////////////////////////////////////////////////
//Name..........: DT_Explode
//Description...: Function factorize date .
//Syntax........: DT_Explode($Date)
//Parameters....: $Date - date
//Return values.: factors
//Author .......: £ukasz Kusy
//Modified......: 14.03.2015
////////////////////////////////////////////////////////////////////////////
function DT_Explode($Date)
{
    return explode("-",$Date);
}
