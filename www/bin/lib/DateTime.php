//Function /////////////////////////////////////////////////////////////////
//Name..........: DT_Age
//Description...: Function count age on the base of date.
//Syntax........: DT_Age($Day, $Month, $Year)
//Parameters....: $Day - day
//                $Month - month
//                $Year - year
//Return values.: $Age - age
//Author .......: £ukasz Kusy
//Modified......: 14.03.2015
////////////////////////////////////////////////////////////////////////////
function DT_Age($Day, $Month, $Year)
{
    $age = date('Y') - $Year;
    if ((date('m') < $Month) || (date('m') == $Month && date('d') < $Day)) {
        $age--;
    }

    return $age ;
}
